package mx.com.amx.unotv.mapelec.dto;

public class TendenciaDTO {
	
	private String name;
	private int tendencia;
	private int id_estado;
	
	/**
	 * @return el name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name el name a establecer
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return el tendencia
	 */
	public int getTendencia() {
		return tendencia;
	}
	/**
	 * @param tendencia el tendencia a establecer
	 */
	public void setTendencia(int tendencia) {
		this.tendencia = tendencia;
	}
	/**
	 * @return el id_estado
	 */
	public int getId_estado() {
		return id_estado;
	}
	/**
	 * @param id_estado el id_estado a establecer
	 */
	public void setId_estado(int id_estado) {
		this.id_estado = id_estado;
	}
	
	
}
